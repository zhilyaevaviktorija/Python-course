greet_text = "Hello, world!"
print(greet_text)

# 1. Заменяю текст в переменной greet_text
greet_text = "Hey"
print(greet_text)

# 2. Создаю новую переменную new_text
new_text = "Today"
print(new_text)

# 3. Записываю new_text в greet_text
greet_text = new_text
print(greet_text)

# 4. Изменяю свойства new_text
new_text += " Wow!"
print(new_text)